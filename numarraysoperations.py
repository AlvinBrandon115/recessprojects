import numpy as np

# Create two arrays
arr1 = np.array([10, 20, 30])
arr2 = np.array([5, 10, 15])

# Addition
print("Addition:")
print(arr1 + arr2)

# Subtraction
print("\nSubtraction:")
print(arr1 - arr2)

# Multiplication
print("\nMultiplication:")
print(arr1 * arr2)

# Division
print("\nDivision:")
print(arr1 / arr2)

# Modulus
print("\nModulus:")
print(arr1 % arr2)

# Exponentiation
print("\nExponentiation:")
print(arr1 ** arr2)